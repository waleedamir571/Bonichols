                                            decisions regarding the child's upbringing. These selections include
                                            educational alternatives, healthcare, non secular upbringing, and
                                            extracurricular activities. In joint criminal custody, both parents share
                                            those duties and must cooperate in making decisions. Sole criminal custody
                                            gives one the distinctive right to make those choices.

