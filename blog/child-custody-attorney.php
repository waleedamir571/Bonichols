                                                Child custody in Houston </a> is categorized into two primary
                                            sorts: felony and bodily. Legal custody refers to the right to make vast
                                            selections about the child's life, such as training and healthcare. Physical
                                            custody determines where the kid will live and the everyday care they
                                            receive. Both forms of custody can be sole, in which one parent has a whole
